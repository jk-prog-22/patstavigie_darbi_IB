# Lietotājs ievada savu vērtējumu programmēšanā izvadīt vārdisko vērtējumu (izcili, teicami utt.)

skaitlis = int(input("Ievadi skaitli:"))
if skaitlis <= 3:
    print("Negatīvs vērtējums")
if skaitlis == 4:
    print("Gandrīz viduvēji")
if skaitlis == 5:
    print("Viduvēji")
if skaitlis == 6:
    print("Gandrīz labi")
if skaitlis == 7:
    print("Labi")
if skaitlis == 8:
    print("Ļoti labi")
if skaitlis == 9:
    print("Teicami")
if skaitlis == 10:
    print("Izcili")